public class Tuile {

    char lettre;
    int valeur;

    public Tuile(char lettre, int valeur) {
        this.lettre = lettre;
        this.valeur = valeur;
    }

    public char getLettre() {
        return lettre;
    }

    public void setLettre(char lettre) {
        this.lettre = lettre;
    }

    public int getValeur() {
        return valeur;
    }

    public void setValeur(int valeur) {
        this.valeur = valeur;
    }



}
